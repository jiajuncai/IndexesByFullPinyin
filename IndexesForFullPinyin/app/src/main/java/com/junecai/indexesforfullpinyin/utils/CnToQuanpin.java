    /**
     * 获得单个汉字的Ascii.
     * @param cn char
     * 汉字字符
     * @return int
     * 错误返回 0,否则返回ascii
     */
    public static int getCnAscii(char cn)
    {
        byte[] bytes = null;
        try
        {
            bytes = (String.valueOf(cn)).getBytes("GBK");
        }
        catch (UnsupportedEncodingException ex)
        {
        }
        if (bytes == null || bytes.length > 2 || bytes.length <= 0)
        {
            //错误
            return 0;
        }
        if (bytes.length == 1)
        {
            //英文字符
            return bytes[0];
        }
        if (bytes.length == 2)
        {
            //中文字符
            int hightByte = 256 + bytes[0];
            int lowByte = 256 + bytes[1];
            int ascii = (256 * hightByte + lowByte) - 256 * 256;
            //System.out.println("ASCII=" + ascii);

            return ascii;
        }

        return 0; //错误
    }

    /**
     * 根据ASCII码到SpellMap中查找对应的拼音
     * @param ascii int
     * 字符对应的ASCII
     * @return String
     * 拼音,首先判断ASCII是否>0&<160,如果是返回对应的字符,
     * 否则到SpellMap中查找,如果没有找到拼音,则返回null,如果找到则返回拼音.
     */
    public static String getSpellByAscii(int ascii, char ch)
    {
        if (ascii > 0 && ascii < 160)
        {
            //单字符
            return String.valueOf( (char) ascii);
        }
        if (ascii < -20319 || ascii > -2050) //-20319           -10247
        {
            //不知道的字符
            return null;
        }

        if (ascii < -2050 && ascii >= -10247) //二级汉字编码,生僻汉字
        {
            //处理二级编码
            String pinyin = (String)spellMapTwo.get("" + ch);
            return pinyin;
        }

        Set keySet = spellMap.keySet();
        Iterator it = keySet.iterator();

        String spell0 = null; ;
        String spell = null;

        int asciiRang0 = -20319;
        int asciiRang;
        while (it.hasNext())
        {
            spell = (String) it.next();
            Object valObj = spellMap.get(spell);
            if (valObj instanceof Integer)
            {
                asciiRang = ( (Integer) valObj).intValue();
                if (ascii >= asciiRang0 && ascii < asciiRang)
                {
                    //区间找到
                    return (spell0 == null) ? spell : spell0;
                }
                else
                {
                    spell0 = spell;
                    asciiRang0 = asciiRang;
                }
            }
        }
        return null;
    }

    /**
     * 判断是否是字母
     * @param ascii int
     * @return boolean
     */
    private static boolean isAlphabet(int ascii)
    {
        if (ascii > 0 && ascii < 160)
        {
            return true;
        }
        else
        {
            return false;
        }
    }

    /**
     * 返回字符串的全拼,是汉字转化为全拼,其它字符不进行转换
     * @param cnStr String
     * 字符串
     * @return String
     * 转换成全拼后的字符串
     */
    public static String getFullSpell(String cnStr)
    {
        if (null == cnStr || "".equals(cnStr.trim()))
        {
            return cnStr;
        }

        boolean isChinese = false;

        char[] chars = cnStr.toCharArray();
        StringBuffer retuBuf = new StringBuffer();
        StringBuffer resultBuf = new StringBuffer();

        for (int i = 0, Len = chars.length; i < Len; i++)
        {
            int ascii = getCnAscii(chars[i]);
            if (ascii == 0)
            {
                //取ascii时出错
                retuBuf.append(chars[i]);
            } else
            {
                String spell = getSpellByAscii(ascii, chars[i]);
                if (spell == null)
                {
                    retuBuf.append("ZZ" + chars[i]);
                }else
                {
                    //System.out.println("spell:"+spell);
                    retuBuf.append(spell);
                } // end of if spell == null
                if (!isAlphabet(ascii))
                {
                    isChinese = true;
                    retuBuf.append("ZZ");
                }
            } // end of if ascii <= -20400
        } // end of for
        if (isChinese)
        {
            //resultBuf.append(cnStr);
            //resultBuf.append("");
            resultBuf.append(retuBuf.toString());
        }else
        {
            resultBuf.append(retuBuf.toString());
        }
        return resultBuf.toString();
    }

